function popSuccess(title, message) {
    Swal.fire (
        title,
        message,
        'success'
    );
}

function popError(title, message) {
    Swal.fire (
        title,
        message,
        'error'
    );
}

function popWarning(title, message) {
    Swal.fire (
        title,
        message,
        'warning'
    );
}